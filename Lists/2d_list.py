
number_list = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
    [0, "s", True]
]

print(number_list)  #It will print list in row wise
print(number_list[0][0])    #It will print 0th row and 0th col value
print(number_list[3][1])    #It will print 2D list's 3rd row and 2st col value
print(number_list[3][2])    #It will print boolean value which is located at 3rd row and 2nd col